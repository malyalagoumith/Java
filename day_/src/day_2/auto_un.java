package day_2;

public class auto_un {
public static void main(String[] args){
	int a=20;
	Integer b=a; // autoboxing(coverts primitive valuea to wrapper object b)
	int c=b; //unboxing (coverts  wrapper object b to primitive value c)
	System.out.println(a);
	System.out.println(b);
	System.out.println(c);

}
}
